import math
from math import *

#print(help(math))

#sqrt: square root
a  = sqrt(64)
print(a)

#isqrt: integer square root
print(isqrt(36))

#pow: power
print(pow(2, 10))
print(pow(27, 1/3))

#ceil: ceiling
print(ceil(3.4))
print(ceil(2.5))

#floor
print(floor(6.2))
print(floor(7.7))

#factorial: giai thừa
print(factorial(10))

#gcd(a, b): greatest common divisor
print(gcd(10, 20))

#comb(n, k): tổ hợp
print(comb(10, 2))

#perm(n, k): chỉnh hợp
print(perm(5, 3))

#fabs()
print(fabs(-199.2443))

#build-in function
print(abs(-24234.434))

#round()
print(round(2.6))

#max(), min(), sum()
print(max(1, 2, 5 ,3))
print(min(1, 2, 5 ,3))
print(sum([1, 2 ,4 ,5 ,6]))